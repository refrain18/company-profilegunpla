                Gunpla Expo adalah acara tahunan di mana akan banyak Gunpla yang dipamerkan, dan tentunya banyak hal menarik yang akan disajikan. Untuk pertama kalinya, pagelaran Gunpla Expo akhirnya datang ke Indonesia, tepatnya di Kota Kasablanka, Jakarta Selatan!

                Acara yang diselenggarakan mulai dari tanggal 25 Oktober sampai 12 November 2017 ini pastinya sangat dinanti-nantikan oleh para penggemar robot plastik dari serial Gundam ini, karena banyak hal menarik, apa saja?

                Berbagai Gunpla dari berbagai lini dan seri diperlihatkan di sini. Mulai dari lini High Grade berskala 1:144 yang didominasi oleh serial Gundam Build Fighters dan Iron Blooded Orphans, ada juga lini Real Grade, dan Master Grade yang memiliki detail serta ukuran berbeda, yaitu 1:100.

                Bersamaan dengan Gunpla Expo, dijual pula Gunpla edisi terbatas yang memang hanya diedarkan saat event berlangsung. Namun sayangnya Gunpla terbatas atau limited itu sudah habis sejak hari pertama.

                Jangan khawatir, karena masih banyak Gunpla dari serial lain yang bisa kamu beli di sini. Selain itu, jika persediaan masih ada, maka kamu akan mendapatkan satu buah HG Customize Weapon secara gratis setiap pembelian satu Gunpla di lokasi, lho.

                Di lokasi juga terdapat layar yang memutarkan cuplikan-cuplikan dari serial Gundam, seperti cuplikan pertarungan di Gundam Build Fighters, cuplikan Mobile Suit dari Gundam Unicorn, maupun iklan Gunpla yang dikemas dengan menarik.
                Pada tanggal 6 November-nya, akan diadakan perlombaan Gundam Builders World Cup 2017 kualifikasi wilayah Indonesia yang juga akan diselenggarakan di lokasi yang sama. Kamu bisa melihat berbagai macam Gunpla yang telah dicustom oleh para peserta dengan sebaik dan sekeren mungkin.

                Hasil dari perlombaan GBWC akan diumumkan pada penghujung event Gunpla Expo, yaitu pada tanggal 12 November 2017. Juara pertama dari GBWC ini akan dikirim ke Jepang untuk mewakili Indonesia!

                Jika kamu penasaran dengan acara Gunpla Expo, datang langsung ya ke lokasi di Food Society Kota Kasablanka, Jakarta Selatan, yang sudah dimulai sejak tanggal 25 Oktober sampai 12 November 2017.</td>
