       Mobile Suit Gundam, seri Gundam pertama yang pernah ditayangkan pada tahun 1979.
       Kurang dari setahun kemudian, kit Gunpla pertama muncul. Dan selama 30 tahun terakhir, 
       orang-orang dari segala umur terus menikmati hobi yang tidak biasa ini.
       Berikut adalah beberapa tonggak sejarah yang menarik dalam sejarah panjang Gunpla:
     <br><br><br>
        Kata "Gunpla" diciptakan bersamaan dengan kit pertama. Seperti "cosplay", ini adalah gabungan bahasa Jepang dari dua kata dalam bahasa Inggris - "Gundam" dan "plastik", seperti pada "model plastik. Penutur bahasa Inggris akan saling menukar ini dengan "model Gundam," tapi kami biasanya mengatakan Gunpla karena lebih pendek.

    Gunpla pertama , yang dirilis pada bulan Juli 1980, harganya hanya 300 yen atau sekitar $ 3. Jelas, ini adalah kit untuk membuat model Mobile Suit Gundam yang pertama.

    Kit Gunpla yang paling awal, yang diluncurkan antara tahun 1980 dan 1989, sama seperti Gunpla yang kita bangun hari ini. Alih-alih menjentikkan bersama, Anda harus merekatkannya dengan semen. Bahkan lebih sulit, mereka semua satu warna, dan Anda perlu melukisnya terlebih dahulu! Anda akan mendapatkan model yang kaku dan tidak dapat dipuji untuk hadiah Anda. Hari ini disebut kit FG (First Grade).

    High Grade pertama dirilis pada tahun 1990 dan harganya 1000 yen atau sekitar $ 10. Seperti yang akan menjadi tradisi, model pertama dari kelas manapun adalah Mobile Suit Gundam yang asli. Hari ini High Grade identik dengan "murah dan mudah," tapi pada tahun 1990 itu dianggap sebagai nilai yang lebih rinci dan dapat dinantikan dari Gunpla.

    Master Grade pertama dirilis pada tahun 1995 dan harganya 2.500 yen atau sekitar $ 25. Ini adalah pertama kalinya Mobile Suit Gundam asli diberi nama dengan benar, seperti RX-78-2. Kelas ini awalnya dirancang untuk digunakan untuk sejumlah model terpilih untuk memperingati ulang tahun Gundam ke-15, namun popularitas model yang lebih akurat ini mengapa produk tersebut masih diproduksi sampai hari ini.

    Yang pertama Perfect Grade Gunpla muncul pada tahun 1998 dan biaya 12.000 yen atau sekitar $ 120. Kelas Sempurna Hari Ini membuat tampilan ini terlihat murahan, sering mencapai $ 200 atau lebih. Nilai sempurna datang dalam skala 1/60 dan mencakup begitu banyak detail sehingga mereka bisa menghabiskan waktu berminggu-minggu untuk membangunnya. Mereka bahkan mungkin datang dengan kabel untuk LED atau fitur elektronik lainnya.

    Pada bulan Juli 2009, Bandai membangun skala 1/1 RX-78-2 untuk memperingati ulang tahun Gundam yang ke 30. Gunpla sejati-ke-skala sekarang berdiri tegak di Odaiba, Tokyo .

    Real Grade pertama muncul baru-baru ini, pada tahun 2010. Ini dirancang untuk menjadi langkah maju dari High Grade dalam hal realisme, tanpa serumit Master Grade. Dalam hal kesulitan, kami menganggap mereka berada di antara keduanya.
